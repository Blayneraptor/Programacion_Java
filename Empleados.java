public class Empleados {
    
    //Atributos

    private String nombre;


    //Constructor

    public Empleados(String nombre) {
        this.nombre = nombre;
    }

    //Getters

    public String getNombre() {
        return nombre;
    }

    //Setters

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }


}


   